package com.subodh.javatraining.javaBasic;

public class BitwiseOperator {
    public static void main(String[] args) {
        int val1 = 1,val2 = 3;
        int val3 = val1 & val2;
        int val4 = val1 | val2;
        int val5 = val1 ^ val2;
        System.out.println(val3);
        System.out.println(val4);
        System.out.println(val5);

        
    }
}

